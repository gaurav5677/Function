package com.gaurav;


public class addTwoNumber {
    public static int sum(int a, int b) {
        int ans = a + b;
        return ans;
    }


    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        int ans = sum(num1, num2);
        System.out.println(ans);

    }
}
